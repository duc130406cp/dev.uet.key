//Swap
#include <bits/stdc++.h>
using namespace std;
#define ll long long
#define ull unsigned long long
#define el "\n"
const int mod=1e9+7;
int main()
{
    ios_base::sync_with_stdio(0);
    cin.tie(0); cout.tie(0);
    int a, b, c = 0;
    cin >> a >> b;
    cout << "a = " << a << ", b = " << b << ", c = " << c << el;
    c = a;
    cout << "a = " << a << ", b = " << b << ", c = " << c << el;
    a = b;
    cout << "a = " << a << ", b = " << b << ", c = " << c << el;
    b = c;
    cout << "a = " << a << ", b = " << b << ", c = " << c << el;
    return 0;
}

